package brand.model;

public class BrandDto {
	
	private String b_num;
	private String h_num;
	private String b_name;
	private String b_photo;
	private String b_addr;
	
	public String getB_num() {
		return b_num;
	}
	public void setB_num(String b_num) {
		this.b_num = b_num;
	}
	public String getH_num() {
		return h_num;
	}
	public void setH_num(String h_num) {
		this.h_num = h_num;
	}
	public String getB_name() {
		return b_name;
	}
	public void setB_name(String b_name) {
		this.b_name = b_name;
	}
	public String getB_photo() {
		return b_photo;
	}
	public void setB_photo(String b_photo) {
		this.b_photo = b_photo;
	}
	public String getB_addr() {
		return b_addr;
	}
	public void setB_addr(String b_addr) {
		this.b_addr = b_addr;
	}
	
	
}
